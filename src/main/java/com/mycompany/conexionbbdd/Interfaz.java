    public void mostrar(){
         try { 
            ps = Conexion.getConnection().prepareStatement("SELECT * FROM cliente");
            rs = ps.executeQuery(); 
            
            while(rs.next()){
                nombre = rs.getString("nombre"); 
                edad = rs.getInt("edad"); 
                ciudad = rs.getString("ciudad"); 
                id = rs.getInt("id_cliente"); 
                
                String mensaje = "ID : " + this.id + " nombre : " + this.nombre + " ciudad: " + this.ciudad + " edad : " + this.edad + "\n"; 
                
                texto = texto + mensaje; 
                
            }
            
            Text.setText(""); 
            Text.setText(texto);
            
        } catch (SQLException ex) {
            Logger.getLogger(Interfaz.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
