    public void mostrarDatos(){
        try { 
            pt = Conexion.getConnection().prepareStatement("SELECT * FROM pedido");
            rs = pt.executeQuery(); 
            
            while(rs.next()){
                fecha = rs.getString("fecha"); 
                id_pedido = rs.getInt("id_pedido"); 
                id_cliente = rs.getInt("id_cliente"); 
                
                String mensaje = "ID : " + this.id_pedido + " nombre : " + this.fecha + " ciudad: " + this.id_cliente  + "\n"; 
                
                texto = texto + mensaje; 
                
            }
            
            jTextArea1.setText(""); 
            jTextArea1.setText(texto);
            
        } catch (SQLException ex) {
            Logger.getLogger(Add_Pedido.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

